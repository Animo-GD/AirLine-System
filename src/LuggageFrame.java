private void LoadTable(){        
        try {
            LoadData ld = new LoadData();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            //Make Sure The Teble Is Clear Before Loading Any Data
            new Airline().clearData(model);
            for(Vector<Object> rowData:ld.getLuggage()){
                model.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(LuggageFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
