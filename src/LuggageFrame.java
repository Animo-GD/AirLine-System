    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Delete Luggage
        try {
            int selectedRow = jTable1.getSelectedRow(); //Return the index of the selected row
            if (selectedRow >= 0) {
                DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
                String item = (String) dt.getValueAt(selectedRow, 0);
                int rowAffected = SaveData.deleteLuggage(item);
                if(rowAffected>0){
                    dt.removeRow(selectedRow);
                }else{
                    JOptionPane.showMessageDialog(this,"Failed To Remove Item!");
                }
                
            } else {
                //If the user click the delete button without selecting any flights
                JOptionPane.showMessageDialog(this, "There is no Item selected!");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Failed To Delete");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Add a new Luggage For the Current User
        String itemName = itemT.getText();
        try{
            float weight = Float.parseFloat(weightT.getText());
            int rowAffected = SaveData.addLuggage(itemName,weight);
            if(rowAffected >= 0){
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                Object[] rowData = {itemName,weight};
                model.addRow(rowData);
                
            }else{
                JOptionPane.showMessageDialog(this,"Faild To Add Item");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Weight should be a number!");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

