    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            //Search For the User
            String pid = passportidT.getText().strip();
            LoadData ld = new LoadData();
            Vector<Object> rowData = ld.findUser(pid);
            if(rowData.isEmpty()){
                JOptionPane.showMessageDialog(this,"Wrong PassportID");
            }else{
                DefaultTableModel model = (DefaultTableModel)table.getModel();
                new Airline().clearData(model);
                model.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(mPassengerFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Update User
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int selectedRow = table.getSelectedRow();
        if(selectedRow>=0){
            try {
                String username = model.getValueAt(selectedRow, 0).toString();
                String password = model.getValueAt(selectedRow, 2).toString();
                String city = model.getValueAt(selectedRow,3).toString();
                String country= model.getValueAt(selectedRow,4).toString();
                boolean admin = Boolean.parseBoolean(model.getValueAt(selectedRow,5).toString());
                int rowAffected = SaveData.updateUser(username, password, city, country, admin);
                if(rowAffected > 0){
                    JOptionPane.showMessageDialog(this,"User Has Been Updated Successfully");
                }else{
                    JOptionPane.showMessageDialog(this,"Failed To Update The User!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(mPassengerFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this,"No User Selected!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

