    /*This Function To Load The Passenger Table From the Database
    First I Have Created a table model to insert into the table with it
    Second I have called the passenger data from loadData class
    Third I have inserted the values into the JTable.
    */
    private void LoadTable(){        
        try {
            LoadData ld = new LoadData();
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            //Make Sure The Teble Is Clear Before Loading Any Data
            new Airline().clearData(model);
            for(Vector<Object> rowData:ld.getPassengerData()){
                model.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(mPassengerFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


