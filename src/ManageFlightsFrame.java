    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Delete Flight
        try {
            int selectedRow = jTable1.getSelectedRow(); //Return the index of the selected row
            if (selectedRow >= 0) {
                DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
                String flightCode = (String) dt.getValueAt(selectedRow, 1);
                System.out.println("Flight Code Of The Selected Row: " + flightCode);
                int rowAffected = SaveData.deleteFlight(flightCode);
                if (rowAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Flight Has Been Deleted");

                    dt.removeRow(selectedRow);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed To Delete This Flight");
                }

            } else {
                //If the user click the delete button without selecting any flights
                JOptionPane.showMessageDialog(this, "There is no flight selected!");
            }
            System.out.println("Selected Row Index: " + selectedRow);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed To Delete");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

