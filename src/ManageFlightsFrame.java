    private void LoadTable() {
        Airline al = new Airline();
        //To Load the flights from the database into the Airline class
        al.setFlight();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //Make Sure The Teble Is Clear Before Loading Any Data
        new Airline().clearData(model);
        for (FlightDescriptions flight : al.getFlight()) {
            Object[] rowData = {
                flight.getArrival().getAirportID(),
                flight.getFlightCode(),
                flight.getArrival().getAirportLocation(),
                flight.getDepartureTime(),
                flight.getDeparture().getAirportLocation(),
                flight.getArrivalTime(),
                flight.getFlightCapacity(),
                flight.getTicketPrice()

            };
            model.addRow(rowData);
        }
    }

