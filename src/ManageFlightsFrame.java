    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {                                         
            // Add Flight
            FlightDescriptions flight = new FlightDescriptions();
            
            String flightCode = flight.getNewFlightCode();
            Date depratureTime = new Date(dTime.getDate().getTime());
            Date arrivalTime = new Date(aTime.getDate().getTime());
            
            String deprature = (String) airportCB.getSelectedItem();
            String airportID = LoadData.findAirportID(deprature);
            String arrival = (String) destCB.getSelectedItem();
            int flightCapacity = 0;
            try {
                
                flightCapacity = Integer.parseInt(capacityT.getText().strip());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Flight Capacity Should Be A Number");
            }
            float ticketPrice = 0;
            try {
                ticketPrice = Float.parseFloat(priceT.getText().strip());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Ticker Price Should Be A Number");
            }
            //Save The new Flight Data
            int rowAffected = SaveData.addFlight(new FlightDescriptions(
                    flightCode,
                    depratureTime,
                    arrivalTime,
                    new AirPort(airportID, deprature),
                    new AirPort(airportID, arrival),
                    flightCapacity,
                    ticketPrice
            ));
            
            if (rowAffected > 0) {
                JOptionPane.showMessageDialog(this, "Flight Has Been Added Successfully");
            } else {
                JOptionPane.showMessageDialog(this, "Failed To Add Flight Try Again!");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(ManageFlightsFrame.class.getName()).log(Level.SEVERE, null,ex);
        }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // Search Flight
            String arrivalCountry = (String) countryCB.getSelectedItem();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            new Airline().clearData(model);
            LoadData ld = new LoadData();
            for (FlightDescriptions flight : ld.searchFlight(arrivalCountry)) {
                Object[] rowData = {
                    flight.getArrival().getAirportID(),
                    flight.getFlightCode(),
                    flight.getArrival().getAirportLocation(),
                    flight.getDepartureTime(),
                    flight.getDeparture().getAirportLocation(),
                    flight.getArrivalTime(),
                    flight.getFlightCapacity(),
                    flight.getTicketPrice()
                        
                };
                model.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageFlightsFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

