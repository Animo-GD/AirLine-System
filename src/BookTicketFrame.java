    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            // Search Flights
            Airline al = new Airline();
            String arrivalCountry = (String) countryCB.getSelectedItem();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            new Airline().clearData(model);
            LoadData ld = new LoadData();
            for (FlightDescriptions flight : ld.searchFlight(arrivalCountry)) {
                Object[] rowData = {
                    flight.getArrival().getAirportID(),
                    flight.getFlightCode(),
                    flight.getArrival().getAirportLocation(),
                    flight.getDepartureTime(),
                    flight.getDeparture().getAirportLocation(),
                    flight.getArrivalTime(),
                    flight.getFlightCapacity(),
                    flight.getTicketPrice()
                        
                };
                model.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(BookTicketFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

