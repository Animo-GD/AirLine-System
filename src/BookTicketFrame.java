    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Book Tickets
        try {
            int selectedRow = jTable1.getSelectedRow(); //Return the index of the selected row
            if (selectedRow >= 0) {
                DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
                String flightCode = (String) dt.getValueAt(selectedRow, 1);
                Date depratureTime =  (Date)dt.getValueAt(selectedRow,3);
                System.out.println("Flight Code Of The Selected Row: " + flightCode);
                int rowAffected = Airline.bookTicket(flightCode);
                SaveData.insertSchFl(flightCode, depratureTime);
                if (rowAffected>0){
                    JOptionPane.showMessageDialog(this,"Thanks For Booking This Ticket, Ticket Code Is: "+flightCode);
                }else{
                    JOptionPane.showMessageDialog(this,"Sorry Failed To Book This Ticket");
                }
            } else {
                JOptionPane.showMessageDialog(this, "There is no flight selected!");
            }
            System.out.println("Selected Row Index: " + selectedRow);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error: "+e);
            System.out.print(e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

